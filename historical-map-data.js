            <p>漢人大量取得平埔族土地之後，產生一種「擠壓」效應，若干平埔族村社往內山與後山（東台灣）遷移，連帶地逼使「番界」不斷往東挪。高山族也受到漢人拓墾浪潮的衝擊。當漢人進逼「番界」時，漢「番」衝突日趨嚴重，清朝政府於是有設隘防番的措施，亦即沿著「番界」設隘（監寮）。隘是一種武裝的防衛機關，負擔防衛工作的監丁，通常由平埔族壯丁充任。隘有監墾，以養監丁，類似自給自足的屯兵制度。設隘原意在隔離番漢，但設隘後漢佃安全比較有保障，於是「隘設墾隨」，墾戶接踵而至，彷彿設隘是爲了拓墾————以防番爲名，行拓墾之實。除了官隘外，也有墾戶自行設隘，以利開墾。道光年間，台灣北部最大的墾隘組織————金廣福大隘，是個由政府鼓勵而成立的民間防番拓墾組織。</p>
